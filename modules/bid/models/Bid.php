    public function getReklamir_r(){
        return $this->hasOne( Reklamir::class, ['id' => 'reklamir_id']);
    }

    public function beforeSave($insert)
    {
        $this->time_id = strtotime( $this->day_id .'-'.$this->mount_id.'-'.$this->year_id . ' ' . ($this->hour_id-1).':'.$this->minute_id );
        return parent::beforeSave($insert); // TODO: Change the autogenerated stub
    }

    public function afterDelete()
    {
        $rec = new BidLog();
        $rec->reklamir_id = $this->reklamir_id;
        $rec->time_id = $this->time_id;


        $rec->save();
        parent::afterDelete(); // TODO: Change the autogenerated stub
    }
}
